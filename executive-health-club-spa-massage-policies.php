                              <p><strong>Making an Appointment</strong></p>
                              <ul>
                                <li>Clients should contact a Health Club Coordinator via telephone or<br>
                                  by stopping into the club for available appointment times.</li>
                                <li>A credit card number, package receipt number, or gift certificate<br>
                                  number is required to book an appointment.</li>
                                <li>A client information form must be signed and fully completed prior<br>
                                to the massage appointment.</li>
                                <li>Advanced Appointments are Necessary.<br>
                                </li>
                              </ul>
                              <p><strong>Cancellations and Time Changes</strong></p>
                              <ul>
                                <li>A minimum of six hours notice is required to cancel or change the<br>
                                time of an appointment.</li>
                                <li>A 50% charge will be applied if less than six hours notice was given<br>
                                  or the client was a &ldquo;No-Show&rdquo;. The charge will be applied to the<br>
                                  credit card given at the time the appointment was made. If the<br>
                                  appointment was secured with a Gift Certificate its value shall be<br>
                                  reduced by 50%. Packages will lose half of a session. In the event the appointment was to be a Gift Certificate with added time, 50% of the total value of the massage will be collected.<br>
                                </li>
                              </ul>
                              <p><strong>Massage Clients</strong></p>
                              <ul>
                                <li>Please check in at the Health Club desk.<br>
                                  The Health Club Coordinator will contact the massage therapist<br>
                                who will escort you to the massage room.</li>
                                <li>Please DO NOT knock on the door of the massage room as you may disturb the session of another client.</li>
                                <li>Please arrive on time, if your appointment is for one hour<br>
                                  beginning at 6pm your appointment begins at 6pm, it ends at 7pm.<br>
                                The massage room is reserved for you during this time.</li>
                                <li>Persons who are neither hotel guest nor Health Club member may<br>
                                  utilize the Health Club facilities on the day of their massage for an<br>
                                additional $5 fee. A waiver must be completed.</li>
                                <li>If as a massage therapy client you feel in any way that the massage<br>
                                  therapy session was unsatisfactory please contact the Health Club<br>
                                  Director within 24hrs of the completion of the massage.<br>
                                  We reserve the right to deny any massage therapy service to any<br>
                                  individual without reason or explanation.<br>
                                </li>
                              </ul>
                              <p><strong>Packages and Gift Certificates</strong></p>
                              <ul>
                                <li>Packages are available for purchase and expire one year after the<br>
                                  date of purchase.</li>
                                <li>Immediately after each session of a package a signature confirming<br>
                                  receipt of the massage is required at the Health Club desk.</li>
                                <li>Packages purchased by Members of the Health Club are to be used<br>
                                  by the individual who purchased the package. The purchaser may<br>
                                  occasionally give a massage session to a friend with Health Club<br>
                                  approval.</li>
                                <li>Packages purchased by Companies (Corporate Packages) or clients who are Non-Members may be used by anyone who has been granted approval by the purchaser.</li>
                                <li>Gift Certificates may be purchased with or without gratuity with<br>
                                  the option for use of the Health Club facilities on the day of the<br>
                                  massage (Massage &amp; Club Package Gift Certificates).</li>
                                <li>Gift Certificates expire six months after the date of purchase.</li>
                              </ul>
